      <h2>Les engagements de l’État</h2>
      <p>
        L’organisateur d’une démarche participative s’engage à respecter une méthode rigoureuse, adaptée aux finalités de la concertation,
        pour permettre aux citoyens de participer dans les meilleures conditions, même s’ils ne connaissent pas ou peu le sujet.
      </p>
      <p>
        Plusieurs règles contenues dans l’article L131-1 du code des relations entre le public et l’administration (CRPA) doivent être
        respectées : garantir à chaque citoyen la possibilité d’exprimer librement son point de vue, respecter sa parole, mettre à sa
        disposition les informations utiles pour comprendre les enjeux du débat et les outils nécessaires pour formuler ses propositions de
